
#include <iostream>
using namespace std;

int main()
{
    char ch;


    cout << "Please type any character: ";
    cin >> ch;


    if (ch == '+')
    {
        cout << "This is an operator" << endl;
    }
    else if (ch == '-')
    {
        cout << "This is an operator" << endl;
    }
    else if (ch == '*')
    {
        cout << "This is an operator" << endl;
    }
    else if (ch == '/')
    {
        cout << "This is an operator" << endl;
    }
    else if (ch == '%')
    {
        cout << "This is an operator" << endl;
    }
    else if (ch == '=')
    {
        cout << "This is an operator" << endl;
    }
    else
    {
        cout << "This is not an operator" << endl;
    }


    return 0;
}
